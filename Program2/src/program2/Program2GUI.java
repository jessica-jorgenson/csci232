    private void update() throws IOException {
        Program2.main(null);
        freqTabl.setText(Program2.freqMapStr);
        binTabl.setText(Program2.binMapStr);
        freqTabl.setVisible(true);
        binTabl.setVisible(true);
        encodedMess.setText("Encoded Message: " + Program2.encode());
        encodedMess.setVisible(true);
        out.setVisible(true);
        pack();
        
    }

