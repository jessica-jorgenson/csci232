    private void asIsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asIsActionPerformed
        String leInput = theInput.getText();
        if (leInput.contains(".")) {
            Program2.getString(true, false, leInput);
        } else {
            Program2.getString(false, false, leInput);
        }
        try {
            update();
        } catch (IOException ex) {
            Logger.getLogger(Program2GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_asIsActionPerformed

